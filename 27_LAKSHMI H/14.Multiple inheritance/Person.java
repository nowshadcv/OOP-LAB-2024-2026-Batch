package MODULE3;

public class Person {
	
	    String name;
	    String gender;
	    String address;
	    int age;

	    Person() {}

	    Person(String name, String gender, String address, int age) {
	        this.name = name;
	        this.gender = gender;
	        this.address = address;
	        this.age = age;
	    }
	

}
